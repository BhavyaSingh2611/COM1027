package ProblemSet_2c;

public class Assessment {
    private String name;
    private double mark;
    public Assessment(String name, double mark) {
        super();
        this.name = name;
        this.mark = mark;
    }

    public String getName() {
        return this.name;
    }

    public double getMark() {
        return this.mark;
    }
}
